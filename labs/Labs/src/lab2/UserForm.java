    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập username", "Lưu ý", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (new String(txtPass.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập password", "Lưu ý", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!rdoUser.isSelected() && !rdoAdmin.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn vai trò", "Lưu ý", JOptionPane.WARNING_MESSAGE);
            return;
        }

        for (User user : list) {
            if (txtUser.getText().equals(user.getUsername())) {
                int choice = (JOptionPane.showConfirmDialog(this, "Bạn muốn cập nhật cho username này?", "Xác nhận", JOptionPane.YES_NO_OPTION));

                if (choice == JOptionPane.YES_OPTION) {
                    user.setPassword(new String(txtPass.getPassword()));
                    user.setRole(rdoUser.isSelected() ? "User" : "Admin");
                    fillTable();
                    JOptionPane.showMessageDialog(this, "Đã cập nhật");
                    btnResetActionPerformed(evt);
                    return;
                } else {
                    btnResetActionPerformed(evt);
                    return;
                }
            }
        }
        
        JOptionPane.showMessageDialog(this, "Không tồn tại username này");
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        int select = tblUser.getSelectedRow();

        if (select >= 0) {
            String username = (String) tblModel.getValueAt(select, 0);
            String password = (String) tblModel.getValueAt(select, 1);
            for (User user : list) {
                if (user.getUsername().equals(username)) {
                    txtUser.setText(username);
                    txtPass.setText(password);
                    if (user.getRole().equals("User")) {
                        rdoUser.setSelected(true);
                    } else {
                        rdoAdmin.setSelected(true);
                    }
                }
            }
        }
    }//GEN-LAST:event_tblUserMouseClicked

