    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập username", "Lưu ý", JOptionPane.WARNING_MESSAGE);
            return;
        }

        for (User user : list) {
            if (txtUser.getText().equals(user.getUsername())) {
                JOptionPane.showMessageDialog(this, "Username này đã tồn tại", "Lưu ý", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        if (new String(txtPass.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập password", "Lưu ý", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!rdoUser.isSelected() && !rdoAdmin.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn vai trò", "Lưu ý", JOptionPane.WARNING_MESSAGE);
            return;
        }

        User user = new User();

        user.setUsername(txtUser.getText());
        user.setPassword(new String(txtPass.getPassword()));
        user.setRole(rdoUser.isSelected() ? "User" : "Admin");

        list.add(user);
        fillTable();
        JOptionPane.showMessageDialog(this, "Đã thêm");
        btnResetActionPerformed(evt);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập username", "Lưu ý", JOptionPane.WARNING_MESSAGE);
            return;
        }

        for (User user : list) {
            if (user.getUsername().equals(txtUser.getText())) {
                int choice = (JOptionPane.showConfirmDialog(this, "Xoá?", "Xác nhận", JOptionPane.YES_NO_OPTION));

                if (choice == JOptionPane.YES_OPTION) {
                    list.remove(user);
                    fillTable();
                    JOptionPane.showMessageDialog(this, "Đã xoá");
                    btnResetActionPerformed(evt);
                    return;
                } else {
                    return;
                }
            }
        }

        JOptionPane.showMessageDialog(this, "Không tồn tại username này");
        btnResetActionPerformed(evt);
    }//GEN-LAST:event_btnRemoveActionPerformed

